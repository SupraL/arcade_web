用戶將對自己的帳戶安全負完全的責任，這包含維護用戶端及帳戶的安全。
即使如此，本工作室仍然會對帳戶安全遭受攻擊或破壞的用戶提供協助。如果帳戶出現以下情況，本工作室工作人員在查證的期間會凍結此帳戶：
