(3) 處分政策
本工作室希望不論是線上論壇或是遊戲內都能提供一個良好的遊戲環境給所有用戶。然而不幸的，違反行為規範的依然會突發。在這時，客戶服務單位將會展開進一步的調查並確保所有當事人享有公平且正當的待遇。依據用戶過去歷史紀錄及事件的嚴重性，客戶服務單位可能將採取以下的動作：
(3.1) 無約束處分。
(3.2) 給予肇事者警告。此處分及詳細原因將會經由電子信箱通知，並且於肇事者帳戶上留下紀錄。
(3.3) 停權，凍結玩家帳戶數小時~14天不等之停權處分。此處分的凍結時間及詳細原因將會經由電子信箱通知。
(3.4) 永久停權，終止玩家帳戶。此處分代表玩家將無期限停權，關於此處分的詳細原因將會經由電子信箱通知。
(3.5) 降低遊戲內一定幅度虛擬的積分、金幣或其他虛擬貨幣、道具、經驗值及其他物品等，關於此處分的詳細原因將會經由電子信箱通知。
(3.6) 刪除帳號，關於此處分的詳細原因將會經由電子信箱通知。
